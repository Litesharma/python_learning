# implicit type conversion
# ---- it is a conversion performed by the compiler without programmer's intervention
# f & int -- conversion to -- float

print(10/5)    # /-- always gives output in float
print(10//5)   #//--- gives in int
print(10.0+5)  # one is f & int -- float


